package taokdao.api.setting.theme.resource;

/**
 * hint 提示性
 * accent 强调性
 */
public class ThemeColors {
    public static final int COLOR_NONE = -1;
    /**
     * 前景色
     */
    public int foregroundColorHint = COLOR_NONE;
    public int foregroundColor = COLOR_NONE;
    public int foregroundColorAccent = COLOR_NONE;
    /**
     * 背景色
     */
    public int backgroundColorHint = COLOR_NONE;
    public int backgroundColor = COLOR_NONE;
    public int backgroundColorAccent = COLOR_NONE;
}
